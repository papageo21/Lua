funcmathf = {}

function funcmathf.add(num1, num2)  
    return num1 + num2
end    
function funcmathf.sub(num1, num2)
    return num1 - num2
end
function funcmathf.pro(num1, num2)
    return num1 * num2
end
function funcmathf.div(num1, num2)
    return num1 / num2
end
function funcmathf.power(num1, num2)
    return num1 ^ num2
end
function funcmathf.id(num1, num2)
    return num1 % num2
end
return funcmathf